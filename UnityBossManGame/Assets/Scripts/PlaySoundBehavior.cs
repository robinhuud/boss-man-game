using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class PlaySoundBehavior : StateMachineBehaviour {
    public AudioClip clip;

    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        // grab an audio source attached to the object we are animating, and stigger our clip if it's not null
        AudioSource au = animator.gameObject.GetComponent<AudioSource>();
        if (au != null)
        {
            au.PlayOneShot(clip);
        }
    }

